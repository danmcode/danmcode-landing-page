filterSelection("mobile");

function filterSelection(filter) {
  let columnGallery, i;
  columnGallery = document.getElementsByClassName("column-gallery");
  if (filter == "all") filter = "";
  for (i = 0; i < columnGallery.length; i++) {
    w3RemoveClass(columnGallery[i], "show");
    if (columnGallery[i].className.indexOf(filter) > -1) w3AddClass(columnGallery[i], "show");
  }
}

function w3AddClass(element, name) {
  let i, arr1, arr2;
  arr1 = element.className.split(" ");
  arr2 = name.split(" ");
  for (i = 0; i < arr2.length; i++) {
    if (arr1.indexOf(arr2[i]) == -1) {element.className += " " + arr2[i];}
  }
}

function w3RemoveClass(element, name) {
  let i, arr1, arr2;
  arr1 = element.className.split(" ");
  arr2 = name.split(" ");
  for (i = 0; i < arr2.length; i++) {
    while (arr1.indexOf(arr2[i]) > -1) {
      arr1.splice(arr1.indexOf(arr2[i]), 1);     
    }
  }
  element.className = arr1.join(" ");
}


